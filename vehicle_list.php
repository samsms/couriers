	function delete_vehicle($id){
		start_load()
		$.ajax({
			url:'ajax.php?action=delete_vehicle',
			method:'POST',
			data:{id:$id},
			success:function(resp){
				if(resp==1){
					alert_toast("Data successfully deleted",'success')
					setTimeout(function(){
						location.reload()
					},1500)

				}
			}
		})
	}
